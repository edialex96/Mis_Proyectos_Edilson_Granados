    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        String nombre=txtnombre.getText();
        String apellido=txtapellido.getText();
        String fecha=txtfecha.getText();
        String email=txtemail.getText();
        String pais=txtpais.getText();
        String ciudad=txtciudad.getText();
        String direccion=txtdireccion.getText();
        String telefono=txttelefono.getText();
        String pass=txtpassword.getText();
        String tipousuario=combotipo.getSelectedItem().toString();
        
    if(nombre.isEmpty()||apellido.isEmpty()||fecha.isEmpty()||email.isEmpty()||pais.isEmpty()||ciudad.isEmpty()||direccion.isEmpty()||telefono.isEmpty()||pass.isEmpty()){
        JOptionPane.showMessageDialog(null, "DEBE COMPLETAR LOS DATOS");
    }else{
        if(tipousuario.equalsIgnoreCase("Seleccionar")){
            JOptionPane.showMessageDialog(null, "DEBE SELLECIONAR UN TIPO DE USUARIO");
        }else{
            try {
                
                String consulta="INSERT INTO usuarios(nombre,apellido,fecha_nacimiento,email,pais,ciudad,direccion,telefono,password,tipo_usuario)VALUES('"+nombre+"','"+apellido+"','"+fecha+"','"+email+"','"+pais+"','"+ciudad+"','"+direccion+"','"+telefono+"','"+pass+"','"+tipousuario+"')";
                PreparedStatement ps=cn.prepareStatement(consulta);
                ps.executeUpdate();
                limpiar();
                JOptionPane.showMessageDialog(null, "DATOS GUARDADOS CORRECTAMENTE");
                
            } catch (Exception e) {
                
                JOptionPane.showMessageDialog(null, "NO SE PUDO GUARDAR USUARIO");
            }
        }
    }
    }//GEN-LAST:event_jButton1ActionPerformed
    
